#include <iostream>

using namespace std;

int main() {
    cout << "7 + 3 = " << 7 + 3 << endl;
    cout << "10 - 5 = " << 10 - 5 << endl;
    cout << "3 * 8 = " << 3 * 8 << endl;
    cout << "22 / 2 = " << 22 / 2 << endl;
    cout << "70.0 / 15.0 = " << 70.0 / 15.0 << endl;
    cout << "9 % 4 = " << 9 % 4 << endl;
    cout << "12 + 5 * 2 = " << 12 + 5 * 2 << endl;
    cout << "(12 + 5) * 2 = " << (12 + 5) * 2 << endl; 
    return 0;
}